class Vehicle:
    def __init__(self, vehicle_id, lat, lon, capacity):
        self.vehicle_id = vehicle_id
        self.lat = lat
        self.lon = lon
        self.capacity = capacity
        self.vehicle_ec = ENERGY_CONFIG.get(capacity).get('Vehicle_EC')
        self.pax_ec = ENERGY_CONFIG.get(capacity).get('Pax_EC')


class Request:
    def __init__(self, lat, lon):
        self.lat = lat
        self.lon = lon


class District:
    def __init__(self, id, wkt):
        self.id = id
        self.wkt = wkt


def main():
    # Connect to database
    conn = sqlite3.connect(FILENAME)
    cur = conn.cursor()

    # Enable spatialite extension
    # conn.enable_load_extension(True)
    # conn.execute("SELECT load_extension('mod_spatialite')")

    # Set start/end time for data generation
    start_time = datetime.strptime(START_TIME, '%Y-%m-%d %H:%M:%S')
    minutes_interval = 15

    starttime = datetime.strftime(start_time, '%Y-%m-%d %H:%M:%S')
    endtime = datetime.strftime(start_time + timedelta(minutes=minutes_interval), '%Y-%m-%d %H:%M:%S')

    # Determine total demand from DB
    query = 'SELECT o_lat, o_lon FROM requests WHERE STRFTIME(o_time) >= STRFTIME(\'{starttime}\') AND ' \
            'STRFTIME(o_time) < STRFTIME(\'{endtime}\')'.format(starttime=starttime, endtime=endtime)

    cur.execute(query)

    global REQUEST_DATA
    global TOTAL_COUNT
    global DISTRICT_SHARE_DICT

    REQUEST_DATA = [Request(x[0], x[1]) for x in cur.fetchall()]
    TOTAL_COUNT = len(REQUEST_DATA)
    print("{} total requests between {} and {}".format(TOTAL_COUNT, starttime, endtime))

    # Load District dict into list
    district_split_list = split_list([District(key, value) for key, value in DISTRICTS.items()], BATCH_SIZE)

    # Calculate number of requests per district to determine vehicle distribution share
    print(datetime.now().strftime("%Y-%m-%d %H:%M:%S") + " Processing {} batches to determine number of requests per district...".format(len(district_split_list)))
    pool = mp.Pool(THREAD_COUNT)
    result = pool.map(calculate_district_share, district_split_list)
    pool.close()
    pool.join()
    print()

    # concatenate result dictionaries
    DISTRICT_SHARE_DICT = {k: v for d in result for k, v in d.items()}

    # determine target amount of vehicles per district
    total_share = 0
    for vehicle_capacity in FLEET_CONFIG:
        total_share = FLEET_CONFIG.get(vehicle_capacity) + total_share

    base_count = FLEET_SIZE * BASE_SHARE
    base_count_per_district = base_count / len(DISTRICTS)
    distribution_count = FLEET_SIZE-base_count

    # determine amount of vehicles per district
    district_target_count = {}
    for district_id, district_target_share in DISTRICT_SHARE_DICT.items():
        cnt = base_count_per_district + district_target_share * distribution_count
        district_target_count.update({district_id: cnt})
        print("District {} vehicle target: {}".format(district_id, cnt))
    print()

    # determine amount of vehicle type per district
    vehicle_distribution_dict = {}
    for vehicle_capacity, vehicle_share in FLEET_CONFIG.items():
        share = vehicle_share/total_share

        vehicle_district_count_dict = {}
        for district_id, district_veh_count in district_target_count.items():
            vehicle_district_count_dict.update({district_id: round(district_veh_count * share)})

        vehicle_distribution_dict.update({vehicle_capacity: vehicle_district_count_dict})

    # generate random location for each vehicle within district and store to list
    vehicle_list = []
    vehicle_id = 1
    for vehicle_capacity, district_distribution in vehicle_distribution_dict.items():
        for district_id, district_count in district_distribution.items():
            for i in range(0, district_count):
                pos = get_random_point_in_polygon(DISTRICTS.get(district_id))
                vehicle_list.append(Vehicle(vehicle_id, pos.y, pos.x, vehicle_capacity))
                vehicle_id = vehicle_id + 1

    # Delete vehicles randomly, if too many were generated
    random.seed()
    while len(vehicle_list) > FLEET_SIZE:
        vehicle_list.pop(random.randint(0, FLEET_SIZE))

    # Generate vehicles randomly, if too few were generated
    while len(vehicle_list) < FLEET_SIZE:
        vehicle_sizes = list(vehicle_distribution_dict.keys())
        size = vehicle_sizes[random.randint(0, len(vehicle_sizes)-1)]
        pos = get_random_point_in_polygon(DISTRICTS.get(random.randint(1, len(DISTRICTS))))
        vehicle_list.append(Vehicle(vehicle_id, pos.y, pos.x, size))
        vehicle_id = vehicle_id + 1

    # write info to csv file
    print(datetime.now().strftime("%Y-%m-%d %H:%M:%S") + " Writing {} vehicles to file".format(len(vehicle_list)))
    filename = "fleet_NYC_{fleetsize}_{fleetconfig}_d{district_count}_{addendum}.csv".format(fleetsize=FLEET_SIZE,
                                                                                  fleetconfig=FLEET_CONFIG,
                                                                                  district_count=len(DISTRICTS),
                                                                                  addendum=ADDENDUM)
    filename = filename.replace(': ', '-')
    with open(filename, 'w', newline='') as out_f:
        print("vehicle_id,capacity,lon,lat,kwh_per_100km,kwh_per_100km_per_pax", file=out_f)
        for veh in vehicle_list:
            print("{},{},{},{},{},{}".format(veh.vehicle_id, veh.capacity, veh.lon, veh.lat, veh.vehicle_ec, veh.pax_ec), file=out_f)


def get_random_point_in_polygon(poly_wkt):
    poly = loads(poly_wkt)
    minx, miny, maxx, maxy = poly.bounds
    while True:
        p = Point(random.uniform(minx, maxx), random.uniform(miny, maxy))
        if poly.contains(p):
            return p


def split_list(big_list, split_size):
    split_size = max(1, split_size)
    return [big_list[i:i + split_size] for i in range(0, len(big_list), split_size)]


def calculate_district_share(district_list: List[District]):
    local_district_share_dict = {}
    # determine relative share of demand in each district
    for district in district_list:
        district_count = 0
        district_poly = loads(district.wkt)

        for request in REQUEST_DATA:
            request_position = Point(request.lon, request.lat)
            if district_poly.contains(request_position):
                district_count = district_count + 1

        print("{} {}: District {} has {} requests".format(datetime.now().strftime("%Y-%m-%d %H:%M:%S"), mp.current_process(), district.id, district_count))
        if district_count > 0:
            share = district_count/TOTAL_COUNT
        else:
            share = 0
        local_district_share_dict.update({district.id: share})

    return local_district_share_dict


